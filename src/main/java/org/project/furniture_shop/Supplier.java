package org.project.furniture_shop;

// Class representing a supplier
public class Supplier {
    private String name;

    // Constructor to initialize the supplier's
    public Supplier(String name) {
        this.name = name;
    }

    // Getter method
    public String getName() {
        return name;
    }

    // Setter method
    public void setName(String name) {
        this.name = name;
    }
}
